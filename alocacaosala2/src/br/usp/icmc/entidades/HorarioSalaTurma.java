package br.usp.icmc.entidades;

public class HorarioSalaTurma {
	int idhor;
	int idsla;
	int idtur;
	
	public HorarioSalaTurma(){
		
	}

	public int getIdhor() {
		return idhor;
	}

	public void setIdhor(int idhor) {
		this.idhor = idhor;
	}

	public int getIdsla() {
		return idsla;
	}

	public void setIdsla(int idsla) {
		this.idsla = idsla;
	}

	public int getIdtur() {
		return idtur;
	}

	public void setIdtur(int idtur) {
		this.idtur = idtur;
	}
	
	
}
