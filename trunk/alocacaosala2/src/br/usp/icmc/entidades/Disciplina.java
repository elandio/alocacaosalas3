package br.usp.icmc.entidades;

public class Disciplina {
	private String iddis;
	private String nomdis;
	private int numcre;
	public String getIddis() {
		return iddis;
	}
	public void setIddis(String iddis) {
		this.iddis = iddis;
	}
	public String getNomdis() {
		return nomdis;
	}
	public void setNomdis(String nomdis) {
		this.nomdis = nomdis;
	}
	public int getNumcre() {
		return numcre;
	}
	public void setNumcre(int numcre) {
		this.numcre = numcre;
	}
	
	
}
