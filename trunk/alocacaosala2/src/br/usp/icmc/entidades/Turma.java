package br.usp.icmc.entidades;

public class Turma {
	private int codtur;
	private int codcur;
	private String coddis;
	private int numalu;
	
	public int getCodtur() {
		return codtur;
	}
	public void setCodtur(int codtur) {
		this.codtur = codtur;
	}
	public int getCodcur() {
		return codcur;
	}
	public void setCodcur(int codcur) {
		this.codcur = codcur;
	}
	public String getCoddis() {
		return coddis;
	}
	public void setCoddis(String coddis) {
		this.coddis = coddis;
	}
	public int getNumalu() {
		return numalu;
	}
	public void setNumalu(int numalu) {
		this.numalu = numalu;
	}
	
	

}
