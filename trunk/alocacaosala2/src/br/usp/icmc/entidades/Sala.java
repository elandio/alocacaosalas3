package br.usp.icmc.entidades;

public class Sala {
	private int idsla;
	private int numsla;
	private int codpav;
	private int capsla;
	
	public Sala(){
		
	}
	
	public int getIdsla() {
		return idsla;
	}
	public void setIdsla(int idsla) {
		this.idsla = idsla;
	}
	public int getNumsla() {
		return numsla;
	}
	public void setNumsla(int numsla) {
		this.numsla = numsla;
	}
	public int getCodpav() {
		return codpav;
	}
	public void setCodpav(int codpav) {
		this.codpav = codpav;
	}
	public int getCapsla() {
		return capsla;
	}
	public void setCapsla(int capsla) {
		this.capsla = capsla;
	}
	
	
}
